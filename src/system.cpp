// Return the system's CPU
Processor& System::Cpu() { return cpu_; }

// Return a container composed of the system's processes
vector<Process>& System::Processes() { 
    vector<int> processid = LinuxParser::Pids();
    foundProcesses.clear();
    for(int i :processid){
        foundProcesses.push_back(i);
    }
    // Sort the processes by the Cpu Utilization
    sort(foundProcesses.begin(), foundProcesses.end(), [](Process& p1, Process& p2){
        return(p2.CpuUtilization() < p1.CpuUtilization());
    });
    return foundProcesses;
}

// Return the system's kernel identifier (string)
